match_substrings <- function(df, v) {

  # Clean numeric representations
  clean_num <- function(x) gsub("[^0-9]", "", x)

  cols <- c("SSN", "PHONE", "ALT_PHONE")

  # Clean df for matching
  df_clean <- df %>%
    mutate(across(all_of(cols), ~ clean_num(as.character(.))))

  # Clean search vector
  v_clean <- clean_num(as.character(v))

  # Reshape into long form
  df_long <- df_clean %>%
    mutate(row_num = row_number()) %>%
    pivot_longer(cols = all_of(cols),
                 names_to = "column",
                 values_to = "value")

  # Perform substring detection
  results <- df_long %>%
    rowwise() %>%
    mutate(
      matched_pattern = paste(
        v_clean[
          vapply(
            v_clean,
            function(p) grepl(p, value, fixed = TRUE),
            FUN.VALUE = logical(1)
          )
        ],
        collapse = ","
      )
    ) %>%
    ungroup() %>%
    filter(matched_pattern != "")

  # Join original df safely using row index
  final <- results %>%
    left_join(
      df, #%>% mutate(row_num = row_number()),
      by = "ID"
    ) %>%
    select(ID, column, value, matched_pattern, everything())

  return(final)
}

